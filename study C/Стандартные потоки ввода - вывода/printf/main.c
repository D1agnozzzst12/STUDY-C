/*

    Спецификаторы преобразования используются в языках программирования, таких как C и C++, для форматирования вывода данных. Они позволяют задавать, как именно данные будут представлены в строке. Давайте рассмотрим каждый из указанного списка подробнее:

    %d или %i:

    Эти спецификаторы используются для отображения целых чисел со знаком (то есть чисел, которые могут быть как положительными, так и отрицательными) в десятичной системе.
    Примеры: printf("%d", -5); выведет -5, а printf("%i", 10); выведет 10.
    %u:

    Этот спецификатор используется для отображения беззнаковых целых чисел (unsigned), которые не могут принимать отрицательные значения, также в десятичной системе.
    Пример: printf("%u", 4294967295); выведет 4294967295.
    %o:

    Используется для отображения беззнакового целого числа в восьмеричной системе счисления.
    Пример: printf("%o", 8); выведет 10 (восьмеричное представление числа 8).
    %x или %X:

    Эти спецификаторы предназначены для отображения беззнаковых целых чисел в шестнадцатеричной системе счисления. %x выводит буквы a-f в нижнем регистре, а %X — в верхнем.
    Пример: printf("%x", 255); выведет ff, а printf("%X", 255); выведет FF.
    %f или %F:

    Эти спецификаторы используются для отображения вещественных чисел (чисел с плавающей запятой) в виде десятичной дроби.
    Пример: printf("%f", 3.14); выведет 3.140000.
    %e или %E:

    Эти спецификаторы служат для вывода вещественных чисел в экспоненциальной форме (научной записи). %e использует строчные буквы "e", а %E — заглавные "E".
    Пример: printf("%e", 12345.678); выведет 1.234568e+04.
    %c:

    Используется для отображения одиночного символа.
    Пример: printf("%c", 'A'); выведет A.
    %s:

    Этот спецификатор предназначен для вывода строк (последовательностей символов).
    Пример: printf("%s", "Hello, World!"); выведет Hello, World!.
    %%:

    Это специальная запись для вывода символа % в форматной строке. 
    Поскольку % используется для спецификаторов, его необходимо дублировать.


    Пример: printf("Discount: 20%%"); выведет Discount: 20%.
    Эти спецификаторы позволяют контролировать форматирование вывода данных, 
    что очень полезно для отладки и представления информации в удобочитаемом виде. 
    Правильное их использование способствует более чистому и понятному коду.

*/ 